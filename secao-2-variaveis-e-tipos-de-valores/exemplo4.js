let num1 = prompt("Digite o número 1: ")
let num2 = prompt("Digite o número 2: ")

// Number(), parserInt(), parseFloart()
let num1Int = parseInt(num1)
let num2Int = parseInt(num2)
let soma = num1Int + num2Int
let sub = num1Int - num2Int
let mult = num1Int * num2Int
let div = num1Int / num2Int

alert("A soma dos dois números é: " + soma)
alert("A subtração dos dois números é: " + sub)
alert("A multiplicação dos dois números é: " + mult)
alert("A divisão dos dois números é: " + div)

